    function changeAmount(form, amountKey = 'amount', oldKey = 'old_currency', currencyKey = 'currency') {
        oldCurrencyChange = form.find('input[name=' + oldKey + ']').val();
        newCurrencyChange = form.find('select[name=' + currencyKey + ']').selectpicker('val');
        oldCurrencyChangeInfo = globalCurrencies[oldCurrencyChange];
        newCurrencyChangeInfo = globalCurrencies[newCurrencyChange];
        amount = getAmount(form.find('input[name=' + amountKey + ']').val());
        amount = BigNumber(amount).toFixed(oldCurrencyChangeInfo['round_payment']);
        amount = getAmount(amount);
        if (newCurrencyChangeInfo['currency'] == oldCurrencyChangeInfo['currency']) {
            newAmount = amount;
        } else if (oldCurrencyChangeInfo['usd_equal']) {
            rate = globalRates[newCurrencyChangeInfo['currency']];
            newAmount = BigNumber(amount).dividedBy(rate).toFixed(newCurrencyChangeInfo['round_payment']);
        } else if (newCurrencyChangeInfo['usd_equal']) {
            rate = globalRates[oldCurrencyChangeInfo['currency']];
            newAmount = BigNumber(amount).multipliedBy(rate).toFixed(newCurrencyChangeInfo['round_payment']);
        } else {
            rate = globalRates[oldCurrencyChangeInfo['currency']];
            amountInUsd = BigNumber(amount).multipliedBy(rate).toFixed(8);
            rate = globalRates[newCurrencyChangeInfo['currency']];
            newAmount = BigNumber(amountInUsd).dividedBy(rate).toFixed(newCurrencyChangeInfo['round_payment']);
        }
        form.find('input[name=' + amountKey + ']').val(newAmount);
    }


    function startProgress() {
        if ($('.blockProgressBar').length) {
            progressEl = $('.blockProgressBar');
            progressElLine = progressEl.find('.blockProgressBarLine');
            progressElLine.css('width', '0%');
            progressEl.removeClass('d-none');
            width = 0;
            progress_id = setInterval(frameProgress, 10);

            function frameProgress() {
                if (width >= 100) {
                    clearInterval(progress_id);
                } else {
                    width = width + 0.5;
                    progressElLine.css('width', width + '%');
                }
            }
        }
    }

    function stopProgress() {
        if ($('.blockProgressBar').length) {
            clearInterval(progress_id);
            progressEl.addClass('d-none');
        }
    }


    function removeClassStartingWith(node, begin) {
        node.removeClass(function(index, className) {
            return (className.match(new RegExp("\\b" + begin + "\\S+", "g")) || []).join(' ');
        });
    }

    function scrollToElement(el) {
        topMargin = parseInt(el.position()["top"]);
        if ($('header').length) {
            topMargin = topMargin - parseInt($('header').height()) - 8;
        }
        if (topMargin < 0)
            topMargin = 0;
        window.scrollTo(0, topMargin);
    }

    function openNewInModal(el) {
        startProgress()
        $.get(el.data('href') + '?no_ajax=1', function(data) {
            stopProgress();
            specificContent = $(data);
            $('#modalNew .blog-detail').html(specificContent.find('.front-page-block  .blog-detail').html());
