            .html());
            $('#modalNew .modal-blog-detail-top-info-date').html(specificContent.find(
                '.front-page-blog-detail-top-block .front-page-blog-detail-top-info-date').html());
            $('#modalNew').modal('show');
        });
        return false;
    }
